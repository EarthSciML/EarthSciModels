def sigma_expr(sp, bin_i):
    """Return ESM expression for sigma_sp at wavelength bin bin_i."""
    d = SIGMA_PHI[sp]
    if "sigma_const" in d:
        return float(d["sigma_const"][bin_i])
    T_grid = d["T_grid"]
    n = len(T_grid)
    table = [d["sigma"][k][bin_i] for k in range(n)]
    return fn("interp.linear", C(table), C(T_grid), "T")


def phi_O31D_expr(bin_i):
    d = SIGMA_PHI["phi_O31D"]
    T_grid = d["T_grid"]
    n = len(T_grid)
    table = [d["phi_per_T"][k][bin_i] for k in range(n)]
    return fn("interp.linear", C(table), C(T_grid), "T")


def j_rate_expr(sigma_sp, branch, phi_type="const"):
    """Build j_rate = sum_i(F_wl_i * sigma_sp_i) * phi, or with phi_O31D."""
    if phi_type == "O31D":
        terms = [Op("*", WL_NAMES[i], f"sigma_{sigma_sp}_{i+1}", f"phi_O31D_{i+1}")
                 for i in range(18)]
        return Op("+", *terms)
    else:
        terms = [Op("*", WL_NAMES[i], f"sigma_{sigma_sp}_{i+1}") for i in range(18)]
        s = Op("+", *terms)
        if branch is None:
            return s
        else:
            return Op("*", s, float(branch))


