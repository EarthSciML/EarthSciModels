# Interpolation helpers (for computing reference values in tests)
# ---------------------------------------------------------------------------

def interp_linear(table, axis, x):
    """1D linear interpolation with flat extrapolation."""
    n = len(axis)
    if x <= axis[0]:
        return table[0]
    if x >= axis[-1]:
        return table[-1]
    for i in range(n - 1):
        if axis[i] <= x <= axis[i + 1]:
            frac = (x - axis[i]) / (axis[i + 1] - axis[i])
            return table[i] + frac * (table[i + 1] - table[i])
    return table[-1]


def interp_bilinear(table_2d, axis_x, axis_y, x, y):
    """2D bilinear interpolation with flat extrapolation.
    table_2d[i][j] = value at (axis_x[i], axis_y[j])
    """
    nx, ny = len(axis_x), len(axis_y)
    # clip to grid bounds (flat extrapolation)
    xc = max(axis_x[0], min(axis_x[-1], x))
    yc = max(axis_y[0], min(axis_y[-1], y))
    # find x bracket
    ix = 0
    for i in range(nx - 1):
        if axis_x[i] <= xc <= axis_x[i + 1]:
            ix = i
            break
    else:
        ix = nx - 2
    # find y bracket
    iy = 0
    for j in range(ny - 1):
        if axis_y[j] <= yc <= axis_y[j + 1]:
            iy = j
            break
    else:
        iy = ny - 2
    # bilinear blend
    fx = (xc - axis_x[ix]) / (axis_x[ix + 1] - axis_x[ix]) if axis_x[ix + 1] != axis_x[ix] else 0.0
    fy = (yc - axis_y[iy]) / (axis_y[iy + 1] - axis_y[iy]) if axis_y[iy + 1] != axis_y[iy] else 0.0
    v00 = table_2d[ix][iy]
    v01 = table_2d[ix][iy + 1]
    v10 = table_2d[ix + 1][iy]
    v11 = table_2d[ix + 1][iy + 1]
    return (v00 * (1 - fx) * (1 - fy) + v10 * fx * (1 - fy) +
            v01 * (1 - fx) * fy + v11 * fx * fy)


def sigma_at_T(sp, bin_i, T):
    """Evaluate sigma for species sp at wavelength bin bin_i and temperature T."""
    d = SIGMA_PHI[sp]
    if "sigma_const" in d:
        return d["sigma_const"][bin_i]
    T_grid = d["T_grid"]
    sigma_table = [d["sigma"][k][bin_i] for k in range(len(T_grid))]
    return interp_linear(sigma_table, T_grid, T)


def phi_O31D_at_T(bin_i, T):
    d = SIGMA_PHI["phi_O31D"]
    T_grid = d["T_grid"]
    phi_table = [d["phi_per_T"][k][bin_i] for k in range(len(T_grid))]
    return interp_linear(phi_table, T_grid, T)


def compute_j_rates(fluxes, T, P, H2O):
    """Compute all j rates given 18 actinic fluxes and atmospheric conditions."""
    result = {}
    # Standard j-rates (const phi=1.0, optional branch ratio)
    for j_var, sigma_sp, _phi_type, branch in J_RATE_SPECS:
        j = sum(fluxes[i] * sigma_at_T(sigma_sp, i, T) for i in range(18))
        if branch is not None:
            j *= branch
        result[j_var] = j
    # j_O31D: sigma_O3 * phi_O31D(T)
    j_O31D = sum(fluxes[i] * sigma_at_T("O3", i, T) * phi_O31D_at_T(i, T) for i in range(18))
    result["j_O31D"] = j_O31D
    # adjust_j_o31D
    A = 6.02e23
    R = 8.314e6
    nd = A * P / (R * T)
    C_H2O = H2O * 1e-9 * nd
    C_O2 = 0.2095 * nd
    C_N2 = 0.7808 * nd
    C_H2 = 0.5e-6 * nd
    RO1DplH2O = 1.63e-10 * math.exp(60.0 / T) * C_H2O
    RO1DplH2 = 1.2e-10 * C_H2
    RO1DplN2 = 2.15e-11 * math.exp(110.0 / T) * C_N2
    RO1DplO2 = 3.3e-11 * math.exp(55.0 / T) * C_O2
    RO1D = RO1DplH2O + RO1DplH2 + RO1DplN2 + RO1DplO2
    j_O31D_adj = RO1DplH2O / RO1D
    result["j_O31D_adj"] = j_O31D_adj
    result["j_o32OH"] = j_O31D * j_O31D_adj
    result["num_density"] = nd
    result["C_H2O"] = C_H2O
    result["C_O2"] = C_O2
    result["C_N2"] = C_N2
    result["C_H2"] = C_H2
    result["RO1DplH2O"] = RO1DplH2O
    result["RO1DplH2"] = RO1DplH2
    result["RO1DplN2"] = RO1DplN2
    result["RO1DplO2"] = RO1DplO2
    result["RO1D"] = RO1D
    return result


